from __future__ import annotations

import re

REPLACEMENTS = {
    "چه کاری می‌کنی امروز؟": "امروز چیکار می‌کنی؟",
    "چه کاری می کنی امروز؟": "امروز چیکار می‌کنی؟",
    "چه کاری بود که دلم گرفت؟": "چی شده دلت گرفته؟",
    "می‌توانم چه کاری کنم؟": "می‌خوای حرف بزنیم؟",
    "می توانم چه کاری کنم؟": "می‌خوای حرف بزنیم؟",
    "از چه محله‌ای هستی؟": "کجایی‌ای؟",
    "از چه محله ای هستی؟": "کجایی‌ای؟",
    "این شهر از عظمت خاصی برخوردار است": "یه حس خاصی داره",
    "چطور می‌توانم کمکتان کنم": "چی شده؟",
    "چطور می‌توانم کمک کنم": "چی شده؟",
    "در خدمتم": "هستم",
    "می‌باشد": "هست",
    "می باشد": "هست",
    "بسیار": "خیلی",
    "مایل هستی": "دوست داری",
}
PATTERNS = [
    (re.compile(r"چه کاری\s+(?:را\s+)?(?:می‌کنی|می کنی)"), "چیکار می‌کنی"),
    (re.compile(r"می‌توانم\s+چه کاری\s+(?:برای تو\s+)?(?:انجام دهم|بکنم|کنم)"), "می‌خوای حرف بزنیم"),
    (re.compile(r"از\s+چه\s+(?:محله|شهر)\s*ای\s+هستی"), "کجایی‌ای"),
    (re.compile(r"از\s+چه\s+(?:محله|شهر)\s*‌ای\s+هستی"), "کجایی‌ای"),
    (re.compile(r"برخوردار است"), "داره"),
]


def humanize_persian(text: str) -> str:
    out = (text or "").strip()
    for old, new in REPLACEMENTS.items():
        out = out.replace(old, new)
    for pattern, repl in PATTERNS:
        out = pattern.sub(repl, out)
    out = re.sub(r"\s+", " ", out).strip()
    return out
